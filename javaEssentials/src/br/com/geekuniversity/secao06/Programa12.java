package br.com.geekuniversity.secao06;
//Tipos de dados
//Numericos (Interios e Reais) 

public class Programa12 {
	public static void main(String[] args) {
		//	Tipos primarios/primitivos
		// Por padrao, os numeros reais em java sao considerados double
		
		float preco = 23.4f; // 23.40
		double preco2 = 23.4; // 23.4348478568879465498465489
		
		//	Tipos nao primarios/primitivos
		Float preco3 = 44.5f;
		Double preco4 = 44.5;
		
		System.out.println("float -> preco = " + preco);
		System.out.println("double -> preco2 = " + preco2);
		System.out.println("Float -> preco3 = " + preco3);
		System.out.println("Doublw -> preco4 = " + preco4);
		
	}
}
